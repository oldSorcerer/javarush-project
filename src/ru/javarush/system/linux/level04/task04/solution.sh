# Определение основного сетевого интерфейса. В данном примере предполагается, что интерфейс называется 'enp0s3'.
INTERFACE="enp0s3"

# Проверка текущего состояния сетевых интерфейсов
echo "Текущее состояние сетевых интерфейсов:"
ifconfig

# Выключение основного сетевого интерфейса
echo "Выключение интерфейса $INTERFACE..."
sudo ifconfig $INTERFACE down

# Проверка состояния интерфейсов после выключения
echo "Состояние интерфейсов после выключения $INTERFACE:"
ifconfig

# Включение основного сетевого интерфейса
echo "Включение интерфейса $INTERFACE..."
sudo ifconfig $INTERFACE up

# Проверка доступности публичного узла 8.8.8.8 с помощью команды ping
echo "Проверка доступности публичного узла (8.8.8.8):"
ping -c 4 8.8.8.8